package sample;

public class Point {
    private float x;
    private float y;

    public float getX(){
        return this.x;
    } 

    public float getY(){
        return this.y;
    } 

    public void setX(float _x){
        this.x = _x;
    }

    public void setY(float _y){
        this.y = _y;
    }
}